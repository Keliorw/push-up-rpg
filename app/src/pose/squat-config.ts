export interface SquatConfig {
  /** Минимальный score ключевой точки, чтобы считать её видимой. */
  minKeypointScore: number;
  /** Сколько мс поза должна стабильно держаться до positionAcquired. */
  positionHoldMs: number;
  /**
   * Сколько мс терпеть кратковременную потерю позиции (пропажу точек во время
   * движения), НЕ сбрасывая состояние — как у отжиманий.
   */
  gateLostGraceMs: number;
  /** Минимум мс между засчитанными повторами (защита от дребезга). */
  minRepDurationMs: number;

  // --- Гейт «стою в полный рост» ---
  /**
   * Минимальная вертикальность торса: (бёдра.y − плечи.y) / длина торса.
   * Стоя ≈ 1 (торс вертикален), в планке/лёжа ≈ 0, при глубоком наклоне
   * корпуса вперёд падает. Гейт должен держаться и в нижней точке приседа
   * (торс наклоняется, но остаётся ближе к вертикали, чем к горизонтали).
   */
  torsoUprightMinFrac: number;
  /**
   * Нижний допуск зазора таз↔колени в гейте: в глубоком приседе таз может
   * опуститься чуть НИЖЕ колен (зазор слегка отрицательный) — позиция не
   * должна рваться в нижней точке.
   */
  gapGateMinFrac: number;

  // --- Сигнал повтора: зазор таз↔колени ---
  /**
   * Зазор = (колени.y − бёдра.y) / длина торса (плечо–бедро). Стоя бёдра
   * значительно выше колен (зазор ≈ 0.8–1.0); в нижней точке приседа таз
   * опускается к уровню колен (зазор → 0). Сигнал масштабо-инвариантен:
   * при подходе/отходе от камеры пропорции сохраняются и фантомных
   * повторов нет (в отличие от абсолютной просадки таза в пикселях).
   */
  gapDownFrac: number;
  /** Порог возврата вверх (> downFrac — гистерезис). */
  gapUpFrac: number;
  /** EMA-сглаживание зазора (1 = без сглаживания). */
  gapSmoothing: number;
  /**
   * Максимальный разброс колен по высоте |левое.y − правое.y| в долях торса,
   * при котором кадр фазы «вниз» считается приседом. Повтор отклоняется, если
   * БОЛЬШИНСТВО валидных кадров фазы «вниз» превышает порог: в ВЫПАДЕ колени
   * разъезжаются на всю фазу (замерено 0.7–0.96), а у приседа — максимум
   * шумовые выбросы (полуоборот к камере даёт до ~0.6, ровный присед ≤0.2).
   * Мгновенная проверка на входе в «вниз» не работает: MoveNet у нижней точки
   * часто теряет колени и шумит.
   */
  maxKneeSplitFrac: number;
  /**
   * Максимально правдоподобный |зазор|. Больше — «мусорный» скелет со
   * схлопнувшимся торсом (длина → 0, зазор взрывается); кадр игнорируется.
   */
  maxPlausibleGap: number;

  // --- Вторичные сигналы (отладка/калибровка по видео) ---
  /** EMA-коэффициент угла колена (1 = без сглаживания). */
  kneeAngleSmoothing: number;
  /** EMA-сглаживание просадки таза. */
  descentSmoothing: number;
  /** Скорость сползания базовой (верхней) линии таза. */
  baselineRelaxAlpha: number;
  /** Максимально правдоподобная просадка таза (в долях торса). */
  maxPlausibleDescent: number;
}

