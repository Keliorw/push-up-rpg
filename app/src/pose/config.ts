export interface DetectorConfig {
  /** Минимальный score ключевой точки, чтобы считать её видимой. */
  minKeypointScore: number;
  /** Сколько мс поза должна стабильно держаться до positionAcquired. */
  positionHoldMs: number;
  /**
   * Сколько мс терпеть кратковременную потерю позиции (пропажу точек во время
   * движения), НЕ сбрасывая состояние. Без этого каждый «моргнувший» кадр
   * рвал бы позицию и перезапускал удержание — и реальные повторы не считались.
   */
  gateLostGraceMs: number;
  /** Минимум мс между засчитанными повторами (защита от дребезга). */
  minRepDurationMs: number;

  // --- Гейт «горизонтальное положение» (планка) ---
  /**
   * Минимальный угол корпуса (плечо–бедро–колено), при котором тело считается
   * прямым (планка). Сидя на стуле бедро согнуто (~90–120°) и гейт не проходит.
   * Применяется, только когда видны бёдра и колени; иначе — запасной гейт.
   */
  plankBodyMinAngleDeg: number;
  /**
   * Максимальное отношение высота/ширина рамки тела для планки. Стоящий человек
   * вытянут вертикально (отношение ~4–5) — это НЕ планка (отсекает наклоны стоя
   * и касания ног). Настоящая планка сплюснута/руки в стороны (~1.5).
   */
  maxBodyAspect: number;
  /**
   * Упор лёжа = ноги ДАЛЬШЕ всего от камеры и ЗА туловищем. Сигнал:
   * (плечи.y − колени.y) / длина торса. С камеры на полу близкие приподнятые
   * плечи проецируются в кадре ВЫШЕ далёких колен на полу, поэтому в реальной
   * планке сигнал отрицательный (замерено −2.6…−0.7). Когда человек встаёт или
   * стоит на коленях, плечи взлетают высоко над коленями — сигнал резко падает
   * (замерено −3.4…−5). Планка = сигнал внутри полосы [min, max].
   */
  legsBehindMinFrac: number;
  /** Верх полосы (запас на другой наклон камеры, когда колени выше плеч). */
  legsBehindMaxFrac: number;
  /**
   * В планке фаза «вниз» требует ещё и согнутого локтя (≤ этого угла), если
   * руки видны. Уникальный признак реального повтора: внизу локоть 119–137°,
   * а при вставании из упора руки остаются прямыми (173–177°) — проседание
   * пересекает порог, но локоть не сгибается, и фантомный повтор не считается.
   */
  plankElbowBendMaxDeg: number;

  // --- Сигнал повтора №1: вертикальное проседание корпуса (основной) ---
  /**
   * Насколько плечи должны опуститься относительно «верхней» позиции, в долях
   * длины торса (плечо–бедро), чтобы засчитать фазу «вниз». Нормировка по длине
   * торса делает сигнал независимым от расстояния до камеры.
   */
  descentDownFrac: number;
  /** Порог возврата вверх (< downFrac — гистерезис). */
  descentUpFrac: number;
  /** EMA-сглаживание сигнала проседания (1 = без сглаживания). */
  descentSmoothing: number;
  /** Скорость медленного «сползания» базовой (верхней) линии за корпусом. */
  baselineRelaxAlpha: number;
  /**
   * Максимально правдоподобное проседание (в долях длины торса). Больше — это
   * мусорная поза (MoveNet «придумал» скелет на фоне, точки схлопнулись, длина
   * торса → 0, проседание взрывается). Такой кадр игнорируется, не считается.
   */
  maxPlausibleDescent: number;

  // --- Сигнал повтора №2: угол локтя (запасной, для вида сбоку/сидя) ---
  /** Угол локтя, при котором руки считаются выпрямленными. */
  elbowExtendedDeg: number;
  /** Угол локтя, при котором руки считаются согнутыми. */
  elbowFlexedDeg: number;
  /** EMA-коэффициент нового замера угла (1 = без сглаживания). */
  angleSmoothing: number;
}

